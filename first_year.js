function first_CSE_score() {
  let tam = document.querySelector(".Tamil-input").value;
  let eng = document.querySelector(".English-input").value;
  let mat = document.querySelector(".Maths-input").value;
  let phy = document.querySelector(".Physics-input").value;
  let chem = document.querySelector(".Chemistry-input").value;
  let BEEE = document.querySelector(".BEEE-input").value;
  let EG = document.querySelector(".EG-input").value;
  let CP = document.querySelector(".C-P-input").value;
  let result =
    (tam * 1 +
      eng * 2 +
      mat * 4 +
      phy * 3 +
      chem * 3 +
      BEEE * 3 +
      EG * 2 +
      CP * 3) /
    21;
  let roundedresult = parseFloat(result.toFixed(3));
  console.log(result);
  document.querySelector(
    ".result-p"
  ).innerHTML = `Your SGPA = ${roundedresult}`;
}
